# Escribir un programa en el cual se ingresen cuatro números, 
# calcular e informar la suma de los dos primeros y el producto del tercero y el cuarto.

number1 = int(input("Introduzca el primer numero:"))
number2 = int(input("Introduzca el segundo numero:"))
number3 = int(input("Introduzca el tercero numero:"))
number4 = int(input("Introduzca el cuarto numero:"))

addition = number1 + number2
multiplication = number3 * number4

print("La suma de los dos primeros es", addition)
print("El producto de los dos ultimos es", multiplication)